def build_edges(H, Kf):
    """All directed edges of H*, with index mapping."""
    edges = []; e2i = {}
    for i in range(len(Kf)):
        for j in range(len(Kf)):
            if H[i][j]:
                e = (Kf[i], Kf[j])
                e2i[e] = len(edges)
                edges.append(e)
    return edges, e2i


def cycle_to_zvec(cycle, e2i, m):
    """Simple directed cycle -> Z-vector in Z^E."""
    v = [0] * m
    N = len(cycle)
    for k in range(N):
        e = (cycle[k], cycle[(k+1) % N])
        if e in e2i: v[e2i[e]] += 1
    return v


