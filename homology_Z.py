def compute_rho_Z(L_star, verbose=True):
    """Compute the Homological Livelock Algebra A = (C_1^+, rho(E), {z^s})."""

    H_raw, Kf = _build_H(L_star)
    H, dead = refine_H(H_raw, Kf, L_star)
    edges, e2i = build_edges(H, Kf)
    nE = len(edges); nV = len(Kf)

    if verbose: print(f"\n  H*: |V|={nV}, |E|={nE}, k={nE - nV + 1}")

    # Shadow determinism check
    ow = defaultdict(list)
    for t in L_star: ow[(t[1], t[2])].append(t)
    is_det = all(len(v)==1 for v in ow.values())
    if verbose: print(f"  Shadow map deterministic: {is_det}")

    # Simple cycles and Z-vectors
    simple = find_simple_cycles(H, Kf)
    svecs = [cycle_to_zvec(sc, e2i, nE) for sc in simple]
    if verbose: print(f"  Simple directed cycles: {len(simple)}")

    # Z-basis (shortest first)
    idx = sorted(range(len(simple)), key=lambda i: len(simple[i]))
    bo, _, rank = z_row_reduce([svecs[i] for i in idx])
    bidx = [idx[i] for i in bo]
    bcyc = [simple[i] for i in bidx]
    bvec = [svecs[i] for i in bidx]
    k = len(bcyc)

    if verbose:
        print(f"  Z-basis dimension: k = {k} (expected {nE-nV+1}) "
              f"{'OK' if k==nE-nV+1 else 'MISMATCH'}")
        for i, bc in enumerate(bcyc):
            print(f"    B[{i}] (len={len(bc)}): "
                  f"{' -> '.join(f'({t[0]},{t[1]},{t[2]})' for t in bc)} ->")

    # Decompose all simple cycles
    scoords = {}
    for si, sv in enumerate(svecs):
        c = z_decompose(sv, bvec)
        if c is not None: scoords[si] = c
    if verbose:
        print(f"  Decomposition: {len(scoords)}/{len(simple)} "
              f"{'ALL' if len(scoords)==len(simple) else 'INCOMPLETE'}")

    # rho(E)
    rho = [[0]*k for _ in range(k)]
    shifts = {}
    if verbose: print(f"\n  rho(E):")
    for bi, bc in enumerate(bcyc):
        walks = enabling_walks(bc, L_star, H, Kf, max_walks=10)
        if not walks:
            if verbose: print(f"    B[{bi}]: no walk"); continue
        w = walks[0]; W = len(w)
        iv = cycle_to_zvec(w, e2i, nE)
        ic = z_decompose(iv, bvec)
        if ic is None:
            if verbose: print(f"    B[{bi}]: image not in span"); continue
        for bj in range(k): rho[bi][bj] = ic[bj]
        act = [(j, ic[j]) for j in range(k) if ic[j] != 0]
        if len(act)==1 and act[0][1]==1 and len(bcyc[act[0][0]])==W:
            bj = act[0][0]; tgt = bcyc[bj]
            for s in range(W):
                if all(w[p]==tgt[(p+s)%W] for p in range(W)):
                    shifts[(bi,bj)] = s; break
        if verbose:
            parts = []
            for j,c in act:
                if c==1: parts.append(f"B[{j}]")
                elif c==-1: parts.append(f"-B[{j}]")
                else: parts.append(f"{c}*B[{j}]")
            sh = f", z^{shifts[(bi,act[0][0])]}" if len(act)==1 and (bi,act[0][0]) in shifts else ""
            print(f"    E(B[{bi}]) = {' + '.join(parts) if parts else '0'}{sh}")

    if verbose:
        print(f"\n  rho(E) ({k}x{k} over Z):")
        for i in range(k):
            print(f"    [{' '.join(f'{rho[i][j]:2d}' for j in range(k))}]")

    # Z-linearity check
    nc = nk = 0
    for si in range(len(simple)):
        if si not in scoords: continue
        src = scoords[si]
        ws = enabling_walks(simple[si], L_star, H, Kf, max_walks=1)
        if not ws: continue
        iv = cycle_to_zvec(ws[0], e2i, nE)
        d = z_decompose(iv, bvec)
        if d is None: continue
        vr = [sum(rho[i][j]*src[i] for i in range(k)) for j in range(k)]
        nc += 1
        if vr == d: nk += 1
        elif verbose: print(f"    FAIL SC[{si}]: rho*src={vr} != direct={d}")
    if verbose:
        print(f"\n  Z-linearity: {nk}/{nc} {'ALL CORRECT' if nk==nc else 'FAILURES'}")

    return {'k': k, 'rho': rho, 'shifts': shifts,
            'basis_cycles': bcyc, 'basis_vecs': bvec,
            'simple': simple, 'simple_coords': scoords}


