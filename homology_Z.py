"""
Livelock Characterization via Directed Cycle Homology over Z
=============================================================

EXACT ALGEBRA
-------------
    A = ( C_1^+(H*), E_rel, {z^s} )

  (1) C_1^+(H*) -- positive cone of H_1(H*, Z), first Grigor'yan path homology.

      H_1(H*, Z) = ker(d_1) / im(d_2)
        d_1(e_{ij}) = e_j - e_i
        d_2(e_{ijk}) = e_{jk} - e_{ik} + e_{ij}

      For triangle-free H*: im(d_2) = 0, H_1 = ker(d_1) = Z^k.
      k = |E| - |V| + c   (c = connected components of refined H*).

      C_1^+ = sub-semigroup generated by simple directed cycles.
      Basis: k Z-independent simple cycles B_0,...,B_{k-1}.

  (2) E_rel: propagation RELATION on C_1^+.

      Each basis cycle B_i maps to a SET of possible images (one per
      enabling walk). Each image is a simple cycle expressible in the
      basis as a Z-linear combination. Each mapping is a bijection.

      When shadows have unique witnesses, E_rel is a function (single
      image per basis cycle) and rho(E) is a permutation matrix over Z.
      When shadows have multiple witnesses, E_rel is many-valued.

  (3) z^s: cyclic shift. ALWAYS defined for every mapping.

      The enabling walk for B_i is always a simple cycle SC_j (verified).
      The shift z^s records the rotation: walk = SC_j rotated by s positions.
      This holds whether the basis decomposition of SC_j is a single B[j]
      or a compound sum like -B[1] + B[5] + B[6]. The walk is still a
      concrete simple cycle with a definite starting position.

DEPENDENCIES: livelock_complete.py
"""

